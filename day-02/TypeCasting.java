public class TypeCasting {
    public static void main(String[] args) {
        int x = 10;
        double y = x;
        System.out.println(y);

        double a = 9.99;
        int b = (int) a;
        System.out.println(b);

        char c = 'Z';
        int ascii = c;
        System.out.println(ascii);
    }
}
